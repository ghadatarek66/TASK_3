#include<stdio.h>
void main(){
	int N,M;
	while(1){
		printf("enter the num ");
		scanf("%d %d",&N,&M);
		if(N<=0 || M<=0){
			break;
		} if (N>M){
			int t=N;
			N=M;
			M=t;
		}int sum=0;
		for(int i=N;i<=M;i++){
			printf("%d ",i);
			sum+=i;
		}
		printf("sum=%d\n",sum);
	}
	
}